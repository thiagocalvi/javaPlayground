package playground;

public class EstruturasCondicionais {
    
    public static void main(String[] args){
        int nota = 30;
        int media = 70;

        if (nota >= media) {
            System.out.println("NOTA: " + nota + ", APROVADO");
        }
        else{
            System.out.println("NOTA: " + nota + ", REPROVADO");
        }

    }
}
